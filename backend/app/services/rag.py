class CorrectionOverridePostprocessor(BaseNodePostprocessor):
    """Mark retrieved nodes that conflict with user corrections.

    When corrections exist, scans each retrieved chunk and prepends
    an override notice if the chunk contains a conflicting value.
    This ensures the LLM sees the conflict explicitly.
    """

    corrections: list[dict] = Field(default_factory=list)

    def _postprocess_nodes(
        self,
        nodes: list[NodeWithScore],
        query_bundle: Optional[QueryBundle] = None,
    ) -> list[NodeWithScore]:
        if not self.corrections:
            return nodes
        for node_ws in nodes:
            text = node_ws.node.get_content().lower()
            for corr in self.corrections:
                old_val = (corr.get("old_value") or "").lower().strip()
                attr = (corr.get("attribute") or "").lower().strip()
                new_val = (corr.get("new_value") or "").lower().strip()
                entity = (corr.get("entity") or "").lower().strip()

                # Match on: old_value, attribute with different value,
                # OR entity name appearing in this chunk
                matched = False
                if old_val and old_val in text:
                    matched = True
                elif attr and attr in text and new_val and new_val not in text:
                    matched = True
                elif entity and entity in text:
                    matched = True

                if matched:
                    prefix = (
                        f"[USER CORRECTED: {corr.get('entity', '?')} — "
                        f"{corr.get('attribute', '?')}="
                        f"{corr.get('new_value', '?')}. "
                        f"IGNORE conflicting values below.]\n"
                    )
                    node_ws.node.set_content(
                        prefix + node_ws.node.get_content()
                    )
                    break
        return nodes


# Toggle: set to False to re-enable QA corpus chunks in retrieval
EXCLUDE_QA_CORPUS = True


class QACorpusFilterPostprocessor(BaseNodePostprocessor):
    """Filter out AI-generated QA corpus chunks from retrieval results.

    QA corpus was generated by GPT-4o-mini and may contain factual errors.
    Checks both metadata filename and content pattern (Q: ... A: ...).
    Toggle EXCLUDE_QA_CORPUS above to enable/disable.
    """

    def _is_qa_chunk(self, node_ws: NodeWithScore) -> bool:
        # Check metadata filename
        filename = (node_ws.node.metadata.get("filename") or "").lower()
        if filename.startswith("qa"):
            return True
        # Check content pattern: AI-generated QA pairs start with "Q: "
        content = node_ws.node.get_content()[:50]
        if content.strip().startswith("Q:") or content.strip().startswith("Q :"):
            return True
        return False

    def _postprocess_nodes(
        self,
        nodes: list[NodeWithScore],
        query_bundle: Optional[QueryBundle] = None,
    ) -> list[NodeWithScore]:
        filtered = [n for n in nodes if not self._is_qa_chunk(n)]
        if len(filtered) < len(nodes):
            logger.info(
                "QACorpusFilter: removed %d QA chunks, kept %d",
                len(nodes) - len(filtered),
                len(filtered),
            )
        return filtered


class ContextStrippingPostprocessor(BaseNodePostprocessor):
    """Strip augmented context metadata from chunks before LLM sees them.

    Chunks stored in pgvector have a "[Ngữ cảnh: ...]" prefix added during
    ingestion for better retrieval. This prefix helps embedding search but
    confuses the LLM during generation (it may cite metadata instead of
    actual content). This postprocessor strips the prefix so the LLM only
    sees the original document content.

    Must run BEFORE SourceNumberingPostprocessor.
    """

    _CONTEXT_PREFIX = re.compile(
        r"^\[Ngữ cảnh:.*?\]\s*",
        re.DOTALL,
    )

    def _postprocess_nodes(
        self,
        nodes: list[NodeWithScore],
        query_bundle: Optional[QueryBundle] = None,
    ) -> list[NodeWithScore]:
        for node_ws in nodes:
            content = node_ws.node.get_content()
            stripped = self._CONTEXT_PREFIX.sub("", content)
            if stripped != content:
                node_ws.node.set_content(stripped)
        return nodes


class SourceNumberingPostprocessor(BaseNodePostprocessor):
    """Number retrieved nodes with clear boundaries so LLM can cite accurately.

    Wraps each node in delimiters with clean separation between
    metadata header and content body:
        --- [Source N] ---
        Tài liệu: filename, trang page
        Nội dung:
        content...
        --- END Source N ---

    Must be the LAST postprocessor in the chain.
    """

    def _postprocess_nodes(
        self,
        nodes: list[NodeWithScore],
        query_bundle: Optional[QueryBundle] = None,
    ) -> list[NodeWithScore]:
        for i, node_ws in enumerate(nodes):
            num = i + 1
            node_ws.node.metadata["source_number"] = num
            original = node_ws.node.get_content()
            filename = node_ws.node.metadata.get("filename", "")
            page = node_ws.node.metadata.get("page", "")
            page_str = f", trang {page}" if page else ""
            node_ws.node.set_content(
                f"--- [Source {num}] ---\n"
                f"Tài liệu: {filename}{page_str}\n"
                f"Nội dung:\n"
                f"{original}\n"
                f"--- END Source {num} ---"
            )
        return nodes


